#!/usr/bin/env node
/**
 * Generates svgDrawings.generated.tsx from OBJECT_IDS and *.svg files in drawings.
 * Run from repo root: node packages/visuals/scripts/generate-drawings.mjs
 * Or from packages/visuals: node scripts/generate-drawings.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const visualsRoot = path.resolve(__dirname, '..');
const drawingsDir = path.join(visualsRoot, 'src', 'objects', 'drawings');
const objectIdsPath = path.join(visualsRoot, 'src', 'objects', 'objectIds.ts');

const objectIdsContent = fs.readFileSync(objectIdsPath, 'utf8');
const objectIdsMatch = objectIdsContent.match(/export const OBJECT_IDS = \[([\s\S]*?)\] as const/);
if (!objectIdsMatch) throw new Error('Could not find OBJECT_IDS in objectIds.ts');
const OBJECT_IDS = [...objectIdsMatch[1].matchAll(/'([^']+)'/g)].map((m) => m[1]);

const svgFiles = fs.readdirSync(drawingsDir).filter((f) => f.endsWith('.svg'));
const baseNames = new Set(svgFiles.map((f) => path.basename(f, '.svg')));

// Primary mapping: objectId -> filename (only if {objectId}.svg exists)
const objectIdToSvg = {};
for (const id of OBJECT_IDS) {
  if (baseNames.has(id)) {
    objectIdToSvg[id] = `${id}.svg`;
  }
}

const lines = [
  '/* Generated by scripts/generate-drawings.mjs - do not edit by hand */',
  "/* eslint-disable @typescript-eslint/no-explicit-any */",
  '',
  "import type { ReactNode } from 'react';",
  "import type { ObjectId } from '../objectIds';",
  '',
];

// Import each SVG as raw string (Vite ?raw)
for (const [id, filename] of Object.entries(objectIdToSvg)) {
  const varName = id.replace(/-/g, '_') + 'Svg';
  lines.push(`import ${varName} from './${filename}?raw';`);
}

lines.push('');

// Data URLs (encodeURIComponent for SVG with possible special chars)
for (const [id] of Object.entries(objectIdToSvg)) {
  const varName = id.replace(/-/g, '_') + 'Svg';
  const dataVar = id.replace(/-/g, '_') + 'DataUrl';
  lines.push(`const ${dataVar} = 'data:image/svg+xml,' + encodeURIComponent(${varName});`);
}

lines.push('');

// DRAWINGS map: id -> () => ReactNode
lines.push('/** Map each object id that has an SVG to a drawing component. Missing ids (marble, strawberry, star, table, phone) have no drawing. */');
lines.push('export const DRAWINGS: Partial<Record<ObjectId, () => ReactNode>> = {');
for (const [id] of Object.entries(objectIdToSvg)) {
  const dataVar = id.replace(/-/g, '_') + 'DataUrl';
  lines.push(
    `  ${id}: () => (
    <g>
      <image href={${dataVar}} x={-20} y={-20} width={40} height={40} preserveAspectRatio="xMidYMid meet" />
    </g>
  ),`
  );
}
lines.push('};');
lines.push('');

lines.push('/** Returns the SVG drawing for a library object id, or null if no SVG. */');
lines.push('export function getSvgDrawing(id: ObjectId): ReactNode {');
lines.push('  const fn = DRAWINGS[id];');
lines.push('  return fn ? fn() : null;');
lines.push('}');
lines.push('');

// Export list of object ids that have drawings (for storybook / UI)
lines.push('/** Object ids that have an SVG drawing in this module. */');
lines.push(
  `export const OBJECT_IDS_WITH_DRAWINGS: ObjectId[] = [${Object.keys(objectIdToSvg).map((id) => `'${id}'`).join(', ')}];`
);

const outPath = path.join(drawingsDir, 'svgDrawings.generated.tsx');
fs.writeFileSync(outPath, lines.join('\n'), 'utf8');
console.log(`Wrote ${outPath} with ${Object.keys(objectIdToSvg).length} drawings.`);
console.log('Missing drawings for:', OBJECT_IDS.filter((id) => !objectIdToSvg[id]).join(', '));
